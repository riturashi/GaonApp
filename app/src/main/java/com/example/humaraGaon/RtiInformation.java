package com.example.humaraGaon;

public class RtiInformation {

    String title;
    String rtiinfo;

    public RtiInformation() {
    }

    public RtiInformation(String title, String rtiinfo) {
        this.title = title;
        this.rtiinfo = rtiinfo;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getRtiinfo() {
        return rtiinfo;
    }

    public void setRtiinfo(String rtiinfo) {
        this.rtiinfo = rtiinfo;
    }
}

